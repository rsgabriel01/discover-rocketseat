// Operadores de comparação

// Irá comparar valores e retornar um Boolean como resposta à comparação

let one = 1
let two = 2

// > maior que
console.log(one > two)

// >= maior ou igual a
console.log(one >= 1)
console.log(two >= 1)

// < menor que
console.log(one < two)

//<= menor ou igual a
console.log(one <= two)
console.log(one <= 1)
